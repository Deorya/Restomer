## Layer Norm

def to_3d(x):
    return rearrange(x, 'b c h w -> b (h w) c')


def to_4d(x, h, w):
    return rearrange(x, 'b (h w) c -> b c h w', h=h, w=w)


class BiasFree_LayerNorm(nn.Module):
    def __init__(self, normalized_shape):
        super(BiasFree_LayerNorm, self).__init__()
        if isinstance(normalized_shape, numbers.Integral):
            normalized_shape = (normalized_shape,)
        normalized_shape = torch.Size(normalized_shape)

        assert len(normalized_shape) == 1

        self.weight = nn.Parameter(torch.ones(normalized_shape))
        self.normalized_shape = normalized_shape

    def forward(self, x):
        sigma = x.var(-1, keepdim=True, unbiased=False)
        return x / torch.sqrt(sigma + 1e-5) * self.weight


class WithBias_LayerNorm(nn.Module):
    def __init__(self, normalized_shape):
        super(WithBias_LayerNorm, self).__init__()
        if isinstance(normalized_shape, numbers.Integral):
            normalized_shape = (normalized_shape,)
        normalized_shape = torch.Size(normalized_shape)

        assert len(normalized_shape) == 1

        self.weight = nn.Parameter(torch.ones(normalized_shape))
        self.bias = nn.Parameter(torch.zeros(normalized_shape))
        self.normalized_shape = normalized_shape

    def forward(self, x):
        mu = x.mean(-1, keepdim=True)
        sigma = x.var(-1, keepdim=True, unbiased=False)
        return (x - mu) / torch.sqrt(sigma + 1e-5) * self.weight + self.bias


class LayerNorm(nn.Module):
    def __init__(self, dim, LayerNorm_type):
        super(LayerNorm, self).__init__()
        if LayerNorm_type == 'BiasFree':
            self.body = BiasFree_LayerNorm(dim)
        else:
            self.body = WithBias_LayerNorm(dim)

    def forward(self, x):
        h, w = x.shape[-2:]
        return to_4d(self.body(to_3d(x)), h, w)


# 动态卷积层
class DynamicFeedForward(nn.Module):
    def __init__(self, dim, ffn_expansion_factor, bias, num_experts=3):
        super(DynamicFeedForward, self).__init__()

        hidden_features = int(dim * ffn_expansion_factor)
        self.hidden_features = hidden_features
        self.num_experts = num_experts

        self.project_in = nn.Conv2d(dim, hidden_features * 2, kernel_size=1, bias=bias)

        # 1. 定义动态权重生成模块 (Attention)
        self.attention = nn.Sequential(
            nn.AdaptiveAvgPool2d(1),  # 全局平均池化 -> (B, C, 1, 1)
            nn.Conv2d(hidden_features * 2, dim, kernel_size=1, bias=False),
            nn.ReLU(),
            nn.Conv2d(dim, self.num_experts, kernel_size=1, bias=False),
            nn.Softmax(1)  # 在 expert 维度上做 softmax
        )

        # 2. 定义多个卷积核作为 "专家"
        kernel_sizes = [3, 5, 7]  # 示例：使用 3x3, 5x5, 7x7 卷积核
        self.experts = nn.ModuleList()
        for k_size in kernel_sizes:
            padding = (k_size - 1) // 2
            self.experts.append(
                nn.Conv2d(hidden_features * 2, hidden_features * 2, kernel_size=k_size,
                          stride=1, padding=padding, groups=hidden_features * 2, bias=bias)
            )

        self.project_out = nn.Conv2d(hidden_features, dim, kernel_size=1, bias=bias)

        print(f"Initialized DynamicFeedForward with {num_experts} experts and kernel sizes {kernel_sizes}")

    def forward(self, x):
        # 输入 x 形状: (B, C, H, W), C=dim
        x = self.project_in(x)  # -> (B, 2*hidden, H, W)

        # 动态计算权重
        weights = self.attention(x)  # -> (B, num_experts, 1, 1)

        # 将每个 expert 应用于输入
        # 分别计算再加权求和，避免显存爆炸
        res = 0
        for i, expert in enumerate(self.experts):
            # 获取当前 expert 的权重, 形状 (B, 1, 1, 1)
            w = weights[:, i:i + 1, :, :]
            # expert(x) -> (B, 2*hidden, H, W)
            # 加权, 利用广播机制
            res = res + expert(x) * w

        # 门控机制和输出
        x1, x2 = res.chunk(2, dim=1)
        x = F.gelu(x1) * x2
        x = self.project_out(x)
        return x


